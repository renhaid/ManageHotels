package com.pojo;

public class SeatInfo {
	private int id;
	private int seatId;
	private String seatPeople;
	private String seatSatus;
	private String remark;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSeatId() {
		return seatId;
	}
	public void setSeatId(int seatId) {
		this.seatId = seatId;
	}
	
	public String getSeatPeople() {
		return seatPeople;
	}
	public void setSeatPeople(String seatPeople) {
		this.seatPeople = seatPeople;
	}
	public String getSeatSatus() {
		return seatSatus;
	}
	public void setSeatSatus(String seatSatus) {
		this.seatSatus = seatSatus;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	

}
